import * as React from "react"

import { cn } from "@/utils/cn"

/**
 * Card is a container component with rounded corners, border, and shadow.
 * It is used to group related content in a visually distinct section.
 */
const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border bg-card text-card-foreground shadow",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

